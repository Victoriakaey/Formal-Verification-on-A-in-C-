#define ROW 100
#define COL 100
#define MAX_QUEUE_SIZE (ROW * COL)

// Directions for neighbor nodes
static const int dRow[] = {-1, 0, 1, 0};
static const int dCol[] = {0, -1, 0, 1};

// Function to calculate Manhattan distance heuristic
static double heuristic(int row, int col, int goalRow, int goalCol) {
  __CPROVER_precondition(row >= 0 && row < ROW, "row in range");
  __CPROVER_precondition(col >= 0 && col < COL, "col in range");
  __CPROVER_precondition(goalRow >= 0 && goalRow < ROW, "goalRow in range");
  __CPROVER_precondition(goalCol >= 0 && goalCol < COL, "goalCol in range");
  return fabs((double)row - (double)goalRow + 0.0) + fabs((double)col - (double)goalCol + 0.0);
}

// Check if a cell is valid for exploration
static bool isValid(int row, int col, int grid[ROW][COL], bool closed[ROW][COL]) {
  __CPROVER_assert(row >= 0 && row < ROW, "Row index within range in isValid");
  __CPROVER_assert(col >= 0 && col < COL, "Col index within range in isValid");
  return (grid[row][col] == 0 && !closed[row][col]);
}

// verify path from goal to start to ensure valid path
static void verify_and_print_path(int parent[ROW][COL][2],
                                  int grid[ROW][COL],
                                  int startRow, int startCol,
                                  int goalRow, int goalCol) {
  // Reconstruct path
  int path[MAX_QUEUE_SIZE][2];
  int length = 0;
  int cr = goalRow;
  int cc = goalCol;
  while (!(cr == startRow && cc == startCol)) {
    __CPROVER_assert(cr >= 0 && cr < ROW, "cr in range during path reconstruction");
    __CPROVER_assert(cc >= 0 && cc < COL, "cc in range during path reconstruction");
    path[length][0] = cr;
    path[length][1] = cc;
    length++;
    int pr = parent[cr][cc][0];
    int pc = parent[cr][cc][1];
    // If parent is -1, we broke the contious test
    __CPROVER_assert(pr >= -1 && pr < ROW, "parent row index valid");
    __CPROVER_assert(pc >= -1 && pc < COL, "parent col index valid");
    cr = pr;
    cc = pc;
  }
  // Add the start cell
  path[length][0] = startRow;
  path[length][1] = startCol;
  length++;

  // Verify each step in the path
  for (int i = 0; i < length; i++) {
    int r = path[i][0];
    int c = path[i][1];
    __CPROVER_assert(r >= 0 && r < ROW, "path cell row in range");
    __CPROVER_assert(c >= 0 && c < COL, "path cell col in range");
    __CPROVER_assert(grid[r][c] == 0, "path cell is walkable");
    if (i < length - 1) {
      // Check adjacency with next cell
      int nr = path[i+1][0];
      int nc = path[i+1][1];
      __CPROVER_assert(( (nr == r && (nc == c+1 || nc == c-1)) ||
                         (nc == c && (nr == r+1 || nr == r-1)) ),
                       "consecutive path cells are adjacent");
    }
  }

  // Print the path length
  printf("Path length: %d\n", length);
  // Print path
  // for (int i = length - 1; i >= 0; i--) {
  //   printf("(%d, %d) -> ", path[i][0], path[i][1]);
  // }
  // printf("\n");
}

// A* search algorithm using static arrays
static void aStar(int grid[ROW][COL], int startRow, int startCol, int goalRow, int goalCol) {
  __CPROVER_assume(startRow >= 0 && startRow < ROW);
  __CPROVER_assume(startCol >= 0 && startCol < COL);
  __CPROVER_assume(goalRow >= 0 && goalRow < ROW);
  __CPROVER_assume(goalCol >= 0 && goalCol < COL);
  __CPROVER_precondition(startRow >= 0 && startRow < ROW, "startRow in range");
  __CPROVER_precondition(startCol >= 0 && startCol < COL, "startCol in range");
  __CPROVER_precondition(goalRow >= 0 && goalRow < ROW, "goalRow in range");
  __CPROVER_precondition(goalCol >= 0 && goalCol < COL, "goalCol in range");

  int queue[MAX_QUEUE_SIZE][2];
  double cost[ROW][COL];
  int parent[ROW][COL][2];
  bool closed[ROW][COL] = { false };

  int front = 0, rear = 0;
  for (int i = 0; i < ROW; i++) {
    for (int j = 0; j < COL; j++) {
      cost[i][j] = DBL_MAX;
      parent[i][j][0] = -1;
      parent[i][j][1] = -1;
    }
  }

  cost[startRow][startCol] = 0.0;
  __CPROVER_assert(rear < MAX_QUEUE_SIZE, "initial enqueue within bounds");
  queue[rear][0] = startRow;
  queue[rear][1] = startCol;
  rear++;

  while (front < rear) {
    __CPROVER_assert(front >= 0 && front < MAX_QUEUE_SIZE, "front in range");
    int currentRow = queue[front][0];
    int currentCol = queue[front][1];
    __CPROVER_assert(currentRow >= 0 && currentRow < ROW, "currentRow in range");
    __CPROVER_assert(currentCol >= 0 && currentCol < COL, "currentCol in range");

    front++;

    // Check if goal is reached
    if (currentRow == goalRow && currentCol == goalCol) {
      __CPROVER_assert(cost[goalRow][goalCol] != DBL_MAX, "If goal reached, cost finite");
      printf("Path Found!\n");

      // Verify the reconstructed path
      verify_and_print_path(parent, grid, startRow, startCol, goalRow, goalCol);
      return;
    }

    closed[currentRow][currentCol] = true;

    // Explore neighbors
    for (int i = 0; i < 4; i++) {
      int newRow = currentRow + dRow[i];
      int newCol = currentCol + dCol[i];

      // Skip invalid
      if (newRow < 0 || newRow >= ROW || newCol < 0 || newCol >= COL) {
        continue;
      }

      if (isValid(newRow, newCol, grid, closed)) {
        double newCost = cost[currentRow][currentCol] + 1.0;
        double h = heuristic(newRow, newCol, goalRow, goalCol);
        double f = newCost + h;
        __CPROVER_assert(newRow >= 0 && newRow < ROW, "newRow in range");
        __CPROVER_assert(newCol >= 0 && newCol < COL, "newCol in range");

        if (f < cost[newRow][newCol]) {
          cost[newRow][newCol] = f;
          parent[newRow][newCol][0] = currentRow;
          parent[newRow][newCol][1] = currentCol;

          __CPROVER_assert(rear >= 0 && rear < MAX_QUEUE_SIZE, "rear in range before enqueue");
          queue[rear][0] = newRow;
          queue[rear][1] = newCol;
          rear++;
        }
      }
    }
  }

  __CPROVER_assert(cost[goalRow][goalCol] == DBL_MAX, "If no path found, cost at goal remains infinite");
  printf("No Path Found!\n");
}

